package gorsat.Commands

import gorsat.Analysis.ColNumAnalysis
import org.gorpipe.gor.session.GorContext


class ColNum extends CommandInfo("COLNUM",
  CommandArguments("", "", 0, 0),
  CommandOptions(gorCommand = true, norCommand = true))
{
  override def processArguments(context: GorContext, argString: String, iargs: Array[String], args: Array[String], executeNor: Boolean, forcedInputHeader: String): CommandParsingResult = {

    val hcols = forcedInputHeader.split("\t", -1)
    var header = hcols(0)
    for (c <- 1 until hcols.length) header += "\t" + (c + 1) + "(" + hcols(c) + ")"

    CommandParsingResult(ColNumAnalysis(), header)
  }
}
