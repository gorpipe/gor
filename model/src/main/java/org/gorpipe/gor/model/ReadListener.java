package org.gorpipe.gor.model;

/**
 * Listener on source reading progress, registered at construction time
 */
public interface ReadListener {
    /**
     * Notify the genomic position currently being read
     *
     * @param chr     The chromosome name
     * @param pos     The position
     * @param prevChr The chromsome reported in last notification message
     * @return True to cancel processing, false to keep on processing
     */
    boolean notify(String chr, int pos, String prevChr);
}
